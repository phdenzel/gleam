def ADU2Mag(data, photzp=30., nan_filter=True):
    """
    Converts MegaCam data to AB magnitude

    Args:
       data <float/np.ndarray> - flux measured from MegaCam observations

    Kwargs:
       photzp <float> - photometric zero-point
       nan_filter <bool> - filter out NaNs from applying log
    """
    data = np.asarray(data)
    scalar = False
    if data.shape is tuple():
        data = np.array([data], dtype=float)
        scalar = True
    transf = -2.5 * np.log10(data) + photzp
    if nan_filter:
        mag_max = np.max(np.nan_to_num(transf))
        loc_nan = np.isnan(transf)
        transf[loc_nan] = mag_max

    if scalar:
        transf = float(transf)
    return transf


def MegaCam2SDSS(data):
    """
    Converts MegaCam's AB magnitudes to SDSS magnitudes

    Args:
       data <list(float)/np.ndarray)> - AB magnitude data of all bands
                                        ordered as (U,G,R,I,I2,Z or U,G,R,I,Z)

    Kwargs:
       None
    """
    data = np.asarray(data)
    MagSDSS = data.copy()
    N = min(data.shape)
    band_idx = data.shape.index(N)
    size = len(data.shape)
    if size > 3:
        raise ValueError("Shape of input not understood!")
    if size == 1 or size == 2 or (size == 3 and band_idx == 0):
        # U band
        MagSDSS[0] = data[0] + 0.181 * (data[0] - data[1])
        # G band
        MagSDSS[1] = data[1] + 0.195 * (data[1] - data[2])
        # R band
        MagSDSS[2] = data[2] + 0.011 * (data[1] - data[2])
        # I band (old)
        MagSDSS[3] = data[3] + 0.079 * (data[2] - data[3])
        if N == 5:
            # Z band
            MagSDSS[4] = data[4] - 0.099 * (data[3] - data[4])
        elif N == 6:
            # I2 band (new)
            MagSDSS[4] = data[3] + 0.001 * (data[2] - data[3])
            # Z band
            MagSDSS[5] = data[5] - 0.099 * (data[3] - data[5])
        else:
            raise IndexError("SDSS transform only possible with all filter bands")
    elif size == 3:
        if band_idx > 0:
            # U band
            MagSDSS[:, :, 0] = data[:, :, 0] + 0.181 * (data[:, :, 0] - data[:, :, 1])
            # G band
            MagSDSS[:, :, 1] = data[:, :, 1] + 0.195 * (data[:, :, 1] - data[:, :, 2])
            # R band
            MagSDSS[:, :, 2] = data[:, :, 2] + 0.011 * (data[:, :, 1] - data[:, :, 2])
            # I band (old)
            MagSDSS[:, :, 3] = data[:, :, 3] + 0.079 * (data[:, :, 2] - data[:, :, 3])
            if N == 5:
                # Z band
                MagSDSS[:, :, 4] = data[:, :, 4] - 0.099 * (data[:, :, 3] - data[:, :, 4])
            elif N == 6:
                # I2 band (new)
                MagSDSS[:, :, 4] = data[:, :, 3] + 0.001 * (data[:, :, 2] - data[:, :, 3])
                # Z band
                MagSDSS[:, :, 5] = data[:, :, 5] - 0.099 * (data[:, :, 3] - data[:, :, 5])
    return MagSDSS


